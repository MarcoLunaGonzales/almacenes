public class TiposCompra {
    private String codTipoCompra = "0";
    private String nombreTipoCompra = "";

    /**
     * @return the codTipoCompra
     */
    public String getCodTipoCompra() {
        return codTipoCompra;
    }

    /**
     * @param codTipoCompra the codTipoCompra to set
     */
    public void setCodTipoCompra(String codTipoCompra) {
        this.codTipoCompra = codTipoCompra;
    }

    /**
     * @return the nombreTipoCompra
     */
    public String getNombreTipoCompra() {
        return nombreTipoCompra;
    }

    /**
     * @param nombreTipoCompra the nombreTipoCompra to set
     */
    public void setNombreTipoCompra(String nombreTipoCompra) {
        this.nombreTipoCompra = nombreTipoCompra;
    }
    
    
}
